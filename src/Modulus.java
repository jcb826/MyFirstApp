public class Modulus {
    public static void main(String[] args) {

        int a = 11;
        int b = 4;
        int result = a % b;
        System.out.println("The remainder of " + a + " divided by " + b + " is " + result);
        System.out.println(4 % 2);
        System.out.println(1245678 % 17);
    }
}
