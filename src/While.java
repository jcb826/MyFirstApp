public class While {
    public static void main(String[] args) {
        int x = 0;
        do {
            System.out.println(x);
            x = x + 1;
        } while (x < 11);

        int y = 10;
        do {
            System.out.println("pony number " + y);
            y = y - 1;

        } while (y >= 1);

        int z = 0;
        while (z < 11) {
            System.out.println(z);
            z = z + 1;
        }


        int a = 0;
        while (a < 11) {
            System.out.println("pony number "+a);
            a = a + 1;
        }


    }
}
